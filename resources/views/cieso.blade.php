@section('texto')
La Fundación CIESO, trabaja en la investigación y desarrollo del tercer sector de la economía. Fundada, el 1 de marzo de 2000, tiene su sede central en Villa María y delegaciones en Rosario, Córdoba y Capital Federal.
<p></p>
CIESO obtuvo convenios con entidades de nivel nacional e internacional y  editorializó numerosos libros sobre la temática. Entre las instituciones que formaron parte de su creación se encuentran: AMMA, SANCOR, la Cooperativa de Electricidad de Hernando, la Cooperativa de Electricidad de Villa Nueva, Fecescor, el Obispado de Villa María, la Federación Agraria, la Universidad Tecnológica Nacional Facultad de Villa María,  la Universidad Nacional de Villa María, la Municipalidad de Villa María y la Municipalidad de Villa Nueva, entre otras.
<p></p>
Las  principales tareas de la Fundación son procurar el estudio en materias de investigación, educación, capacitación, promoción y editorialización  de temáticas vinculadas con el sector de la economía social, como por ejemplo: el asesoramiento en la formación de cooperativas y mutuales a los alumnos de las escuelas secundarias;  la puesta en prácticas de cooperativas de comunicación (radio), de consumo (alimentos), y de  editorial (revista juvenil).
<p></p>
También tiene como función, el desarrollo de concursos con escuelas secundarias a fin de fomentar el sistema cooperativo y el sistema democrático. Se realizan además seminarios para formar investigadores que luego trabajarán en campo sobre diferentes procesos de investigación. Otras acciones impulsadas por CIESO son los ciclos de conferencias, y las presentaciones de editoriales vinculadas al sector de la economía social.

@endsection

